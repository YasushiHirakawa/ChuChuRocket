#include "HLCommon.h"

//---------------------------------------------------------------------------------------------
//	Static Member
//---------------------------------------------------------------------------------------------
wchar_t			HLCommon::window_name_[kStrMax];
HWND			HLCommon::window_handle_;


//---------------------------------------------------------------------------------------------
//	Method
//---------------------------------------------------------------------------------------------
/*---------------------------------------------------------------------------------------------
//	@brief	マルチバイト文字列からワイド文字列に変換
//
//	@param	mbStr	変換したいマルチバイト文字列
//	@param	wStr	変換後の格納用ワイド文字列
---------------------------------------------------------------------------------------------*/
void HLCommon::MultibyteToWide(const char* mbStr, wchar_t* wStr)
{
	size_t	ret;
	wchar_t	w_str[kStrMax];

	setlocale(LC_CTYPE, "jpn");
	mbstowcs_s(&ret, w_str, kStrMax, mbStr, _TRUNCATE);

	wcscpy_s(wStr, ret, w_str);
}

/*---------------------------------------------------------------------------------------------
//	@brief	ウィンドウハンドルの設定
//
//	@param	HWND hWnd	：ウィンドウハンドル
---------------------------------------------------------------------------------------------*/
void HLCommon::SetWindowHandle(HWND hWnd)
{
	// ウィンドウハンドルの設定
	window_handle_ = hWnd;

	// ウィンドウ名の取得
	window_handle_ = hWnd;
	GetClassName(window_handle_, window_name_, kStrMax);
}

